/**
 * 根據當前日期，返回對應的中文星期名稱 (星期一, 星期二, ..., 星期日)
 * @returns {string} 中文星期名稱
 */
function getTodayChineseDay() {
    const dayNames = ["星期日", "星期一", "星期二", "星期三", "星期四", "星期五", "星期六"];
    const date = new Date();
    const dayIndex = date.getDay(); // 0=星期日, 1=星期一, ..., 6=星期六
    
    // 將星期日 (0) 轉換為 "星期七"，因為您的資料使用 "星期七" 表示星期日
    if (dayIndex === 0) {
        return "星期七";
    }
    
    return dayNames[dayIndex];
}